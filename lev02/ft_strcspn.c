int ft_strcspn(const char *s, const char *rej)
{
    int i;
    int i2;

    i = 0;
    while (s[i] != '\0')
    {
        i2 = 0;
        while (rej[i2] != '\0')
        {
            if (s[i] == rej[i2])
                return (i);
            i2++;
        }
        i++;
    } 
    return (i);
}
