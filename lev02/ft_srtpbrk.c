char *ft_strpbrk(const char *s, const char *s2)
{
    int i;
    int i2;

    i = 0;
    while (s[i] != '\0')
    {
        i2 = 0;
        while (s2[i2] != '\0')
        {
            if (s[i] == s2[i2])
                return ((char *)s[i]);
            i2++;
        }
        i++;
    }
    return (0);
}
